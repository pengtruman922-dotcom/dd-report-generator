def get_chunk_prompt(chunk_id: str) -> str:
    """Get system prompt for a specific chunk."""
    prompts = {
        "chunk0": CHUNK0_PROMPT,
        "chunk1": CHUNK1_PROMPT,
        "chunk2": CHUNK2_PROMPT,
        "chunk3": CHUNK3_PROMPT,
        "chunk4": CHUNK4_PROMPT,
        "chunk5": CHUNK5_PROMPT,
        "chunk6": CHUNK6_PROMPT,
        "chunk7": CHUNK7_PROMPT,
    }
    default_prompt = prompts.get(chunk_id, "")
    return get_prompt(chunk_id, default_prompt)
